    let itemRepo = ItemRepository()
    var foods = [Food]()
    
    
    override func viewDidLoad() {
        super.viewDidLoad()
        
        itemRepo.getItems(.food) { (items) in
            self.foods = items as! [Food]
            
            DispatchQueue.main.async {
